public class UsingVariables
{
    public static void main(String args[])
    {
        int ROOM_NUMBER = 110;
        double MY_WEIGHT = 80.45;
        String CURRENT_STATUS = "I am learning Java";

        System.out.println("My room number is " +  ROOM_NUMBER);
        System.out.println("My weight is " + MY_WEIGHT);
        System.out.println("And " + CURRENT_STATUS);
    }
}