public class Weekday
{
    public static void main(String args[])
    {
        int mm = 10;
        int yyyy = 1996;
        String dd = "31";
        String date = month_name(mm) + yyyy + dd;
        System.out.println(date);
    }

    public static int month_name(int number)
    {
        number = 12;
        return number;
    }
}